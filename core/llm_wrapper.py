# core/llm_wrapper.py
"""
LLM wrapper for Playwright browser automation.

Replaces browser-use's LLM subclass with a standalone function that generates
structured Playwright tool calls from natural language step descriptions.

Architecture:
  NL step + DOM snapshot ──→ LLM (DashScope/Qwen) ──→ PlaywrightMCPToolCall (JSON)
"""

import asyncio
import json as _json
import logging
import os
import re
from typing import Any, Optional

import openai
from openai import AsyncOpenAI
from openai.types.chat import ChatCompletionContentPartTextParam
from pydantic import BaseModel, Field
from pydantic import ValidationError as PydanticValidationError

from app.security.encryption import decrypt_value

logger = logging.getLogger(__name__)

# ------------------------------------------------------------------
# Pydantic model for LLM output
# ------------------------------------------------------------------


class VerificationResult(BaseModel):
    """Result of verifying expected result against page state."""

    passed: bool = Field(..., description="Whether the expected result is met")
    reason: str = Field("", description="Brief explanation of the judgment")


class PlaywrightMCPToolCall(BaseModel):
    """A single browser action generated by the LLM from a NL step description."""

    action: str = Field(
        ...,
        description="One of: goto, click, fill, select, wait, screenshot, assert_text",
    )
    selector: Optional[str] = Field(
        None,
        description="CSS/text/XPath selector for the target element",
    )
    selector_type: str = Field(
        default='css',
        description="Selector type: css, text, xpath",
    )
    value: Optional[str] = Field(
        None,
        description="URL for goto, text for fill, option value for select",
    )
    timeout_ms: int = Field(
        default=30000,
        description="Timeout in milliseconds for this action",
    )
    thinking: Optional[str] = Field(
        None,
        description="LLM reasoning about why this action was chosen",
    )
    next_goal: Optional[str] = Field(
        None,
        description="What the agent plans to do after this step",
    )
    needs_verification: bool = Field(
        default=False,
        description="是否需要在执行完成后再次验证页面状态。"
        "设为 true 的场景：操作会影响页面状态（点击按钮后弹窗出现、提交后页面跳转、等待某元素出现等），"
        "需要 LLM 再次检查页面快照确认预期结果是否达成。"
        "设为 false 的场景：操作本身的效果就是预期结果（输入文字到输入框、勾选复选框、选择下拉项等），"
        "MCP 调用的返回就足以判断成功与否。",
    )


class VerificationCondition(BaseModel):
    """A single deterministic check condition parsed from natural-language expected_result.

    Level 1 of the tiered verification cascade — these conditions can be evaluated
    deterministically by the browser without needing further LLM calls.
    """

    check: str = Field(
        ...,
        description="检查类型: text_contains, text_matches, url_contains, url_matches, "
        "element_visible, element_count, page_title, js_expression",
    )
    target: str = Field(
        ...,
        description="检查目标值: 要搜索的字符串、正则表达式、选择器、计数值或 JS 表达式",
    )
    location: Optional[str] = Field(
        None,
        description="可选的位置提示: header, sidebar, main_content, right_sidebar, footer, modal 等",
    )


# ------------------------------------------------------------------
# LLM prompt for tool call generation
# ------------------------------------------------------------------

SYSTEM_PROMPT = """You are a precise browser automation agent using Playwright MCP.

INPUT you will receive:
1. A natural language step description (what the user wants to do)
2. An accessibility snapshot of the current page (shows page structure with refs for each element)
3. The BASE URL for the target application (for navigation steps)

Your job: find the best matching element from the accessibility snapshot and output a single
action to accomplish the step. Each element in the snapshot has a ref (e.g., e12, e45).
Use these refs as the "selector" field to target elements precisely.

ACTIONS available (maps to Playwright MCP tools):
- "goto": Navigate to a URL. selector=null, value=URL.
- "click": Click an element by its ref. selector=ref, value=null.
- "fill": Type text into an input field by ref. selector=ref, value=text.
- "select": Select dropdown option by ref. selector=ref, value=option value/text.
- "wait": Wait for text to appear. selector=null, value=text to wait for.
- "screenshot": Take a full-page screenshot. selector=null, value=filename.
- "assert_text": Wait for text to be visible. selector=null, value=text.
- "snapshot": Refresh page snapshot (no action). selector=null, value=null.

RULES:
- Use element refs from the snapshot (e.g., "e12") as selectors — do NOT invent CSS selectors.
- Output ONLY the JSON object. No markdown fences, no explanation text.
- Always include "thinking" explaining why you chose this action and element.
- For text input fields, match by role="textbox" and accessible name.
- For buttons, match by role="button" and accessible name.
- For links, match by role="link" and text content.
- If no matching element found, use action="error" with explanation in value.
- If navigation step, use action="goto" with the BASE URL as the value, or a full URL if the step explicitly specifies one.

OUTPUT SCHEMA (exact JSON):
{
  "action": "click",
  "selector": "e15",
  "value": null,
  "timeout_ms": 30000,
  "thinking": "Step wants to click submit button. Found button ref=e15 with name 'Submit'.",
  "next_goal": "Verify login success message"
}"""


