VERIFY_PROMPT = """You are a test result verifier. Your job is to check whether the current page state matches the expected result.

INPUT:
1. STEP EXECUTED (optional): The action that was just performed
2. EXPECTED RESULT: What should be true after the step executes
3. PAGE CONTENT: Current accessibility snapshot of the page

OUTPUT: A JSON object with:
- "passed": true if the expected result is clearly met, false otherwise
- "reason": brief explanation (1-2 sentences)

RULES:
- If STEP EXECUTED describes an input/typing action and EXPECTED RESULT says the input was successful (e.g. "输入成功", "输入完成"), check whether the text appears in the input field — do NOT require a submission or confirmation message
- If the expected result mentions specific text/elements, they must be visible in the snapshot
- If the snapshot is empty or unavailable, return passed=false
- Output ONLY the JSON object, no markdown fences

EXAMPLE OUTPUTS:
{"passed": true, "reason": "Search box contains 'berylliumman' as expected"}
{"passed": false, "reason": "Expected success message not visible on page"}
"""


async def verify_expected_result(
    expected_result: str,
    dom_snapshot: str,
    *,
    step_description: str | None = None,
    model: str | None = None,
    temperature: float = 0.1,
    client: AsyncOpenAI | None = None,
) -> VerificationResult:
    """Verify if the current page state matches the expected result.

    Args:
        expected_result: What should be true after step execution
        dom_snapshot: Current page accessibility snapshot
        model: Override default model
        temperature: LLM temperature
        client: Pre-configured OpenAI client

    Returns:
        VerificationResult with passed=True/False and reason
    """
    if client is None:
        client = await create_openai_client()

    _, _, resolved_model = await _resolve_config(explicit_model=model)

    context = f"STEP EXECUTED:\n{step_description}\n\n" if step_description else ""
    user_message = (
        f"{context}"
        f"EXPECTED RESULT:\n{expected_result}\n\n"
        f"PAGE CONTENT (accessibility snapshot):\n"
        f"{dom_snapshot or '(empty page)'}\n\n"
        f"Verify if the expected result is met. Output JSON now."
    )

    messages: list[dict] = [
        {'role': 'system', 'content': VERIFY_PROMPT},
        {'role': 'user', 'content': user_message},
    ]

    try:
        response = await client.chat.completions.create(
            model=resolved_model,
            messages=messages,
            temperature=temperature,
            max_tokens=512,
        )

        content = response.choices[0].message.content or ''
        content = content.strip()

        # Strip markdown fences
        content = re.sub(r'^```(?:json)?\s*', '', content)
        content = re.sub(r'\s*```$', '', content)

        # Try to parse
        try:
            parsed = _json.loads(content)
        except _json.JSONDecodeError:
            # Attempt repair
            match = re.search(r'\{.*\}', content, re.DOTALL)
            if match:
                content = match.group(0)
            content = content.replace("'", '"')
            content = re.sub(r':\s*None\b', ': null', content)
            content = re.sub(r':\s*True\b', ': true', content)
            content = re.sub(r':\s*False\b', ': false', content)
            try:
                parsed = _json.loads(content)
            except _json.JSONDecodeError as e:
                logger.warning("Verification LLM output not valid JSON: %s", content[:200])
                return VerificationResult(passed=False, reason=f"LLM output parse error: {e}")

        return VerificationResult.model_validate(parsed)

    except Exception as exc:  # noqa: BLE001 - 验证 LLM 调用：吞掉所有异常并返回 passed=False
        logger.exception("Verification LLM call failed")
        return VerificationResult(passed=False, reason=f"Verification failed: {exc}")


